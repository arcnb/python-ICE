# collect the integer value of rectangle length from the user
length = int(input('Please enter the rectangle length: '))
# collect the integer value of rectangle breadth from the user
breadth = int(input('Please enter the rectangle breadth: '))
# print the resulting integer value of perimeter for the given rectangle dimensions
print('perimeter of the rectangle with length', length, 'and breadth', breadth, 'is', 2 * (length + breadth))
